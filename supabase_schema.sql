-- 11. Create view for admin statistics dashboard overview
CREATE OR REPLACE VIEW public.admin_overview AS
SELECT 
  -- Total user balance liability
  (SELECT COALESCE(SUM(balance), 0) FROM public.profiles) as total_liability,
  
  -- Orders today
  (SELECT COUNT(*) FROM public.orders WHERE created_at >= CURRENT_DATE) as orders_today,
  
  -- Revenue today (sum of price for completed orders today)
  (SELECT COALESCE(SUM(price), 0) FROM public.orders WHERE status = 'COMPLETED' AND created_at >= CURRENT_DATE) as revenue_today,
  
  -- Cost today
  (SELECT COALESCE(SUM(s.cost_price * 278.50), 0) 
   FROM public.orders o
   JOIN public.services s ON o.product_id = s.service_id
   WHERE o.status = 'COMPLETED' AND o.created_at >= CURRENT_DATE) as cost_today,
  
  -- Lifetime orders
  (SELECT COUNT(*) FROM public.orders) as orders_lifetime,
  
  -- Lifetime revenue
  (SELECT COALESCE(SUM(price), 0) FROM public.orders WHERE status = 'COMPLETED') as revenue_lifetime,
  
  -- Lifetime cost
  (SELECT COALESCE(SUM(s.cost_price * 278.50), 0) 
   FROM public.orders o
   JOIN public.services s ON o.product_id = s.service_id
   WHERE o.status = 'COMPLETED') as cost_lifetime;

